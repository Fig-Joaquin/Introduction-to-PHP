<?php

$numero1 = 100;

$numero2 = 10;

$numero3 = 30;

// * Mayor o Menor
var_dump($numero1 > $numero2);
echo "<br>";

var_dump($numero2 > $numero3);
echo "<br>";

// * Igualdad (Revisa valor)
var_dump($numero1 == $numero3);
echo "<br>";

// * Igualda (Revisa Valor y Tipo de dato)
var_dump($numero1 === $numero3);
echo "<br>";

// * Desigual
var_dump($numero1 != $numero3);
echo "<br>";

// * Mayor o Menor igual
var_dump($numero1 >= $numero2);
echo "<br>";

var_dump($numero1 <= $numero2);
echo "<br>";






